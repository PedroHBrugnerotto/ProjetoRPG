    private void PopulaJtable() {
        String sql = 
         "SELECT id, player, nome, tipo, dano, tamanho FROM Itens";
        try {
            Conector conector = new Conector();
            Connection con =
            conector.getConnection();        
            PreparedStatement banco =
                    con.prepareStatement(sql);
            banco.execute();
            ResultSet resultado =
                    banco.executeQuery(sql);
            DefaultTableModel model = 
                    (DefaultTableModel) 
                    jtItens.getModel();
            model.setNumRows(0);
            while (resultado.next()){
                model.addRow(new Object[]{
                    resultado.getString("ID"),
                    resultado.getString("player"),
                    resultado.getString("Nome"),
                    resultado.getString("Tipo"),
                    resultado.getString("Dano"),
                    resultado.getString("Tamanho")
                });
            }
            banco.close();
            con.close();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
