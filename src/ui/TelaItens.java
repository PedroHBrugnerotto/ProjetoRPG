    private void brnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnSalvarActionPerformed
        String strID = 
                txtID.getText();
        String strNome = 
                txtNome.getText();
        String strTipo = 
                txtTipo.getText();
        String strPlayer = 
                txtDono.getText();
        String strDano = 
                txtDano.getText();
        String strTamanho = 
                txtTamanho.getText();
        
        if(strID.equals("")){
            //Verifica os campos vazios
            if(strNome.equals("") || 
                    strTipo.equals("") ||
                    strDano.equals("")){
                String strMsg = 
                        "Preencha todos"
                    + " os campos para "
                    + "salvar";
                JOptionPane.showMessageDialog(
                        null, strMsg);
            }else{
                //insere no banco
                String sql = "INSERT INTO itens "
                        + "(player, Tipo, Nome,  Tamanho, Dano) "
                        + "VALUES (?, ?, ?, ?, ?)";
                Conector conector = new Conector();
                Connection con = conector.getConnection(); 
                try {
                    PreparedStatement stm =
                            con.prepareStatement(sql);
                    stm.setString(3, strTipo);
                    stm.setString(2, strNome);
                    stm.setString(1, strPlayer);
                    stm.setString(4, strTamanho);
                    stm.setString(5, strDano);
                    stm.execute();
                    stm.close();
                    con.close();
                    JOptionPane.showMessageDialog(
                        null, "Item inserido!");
                    txtTipo.setText("");
                    txtNome.setText("");
                    txtID.setText("");
                    txtTamanho.setText("");
                    txtDono.setText("");
                    txtDano.setText("");
                    PopulaJtable();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(
                        null, ex);
                }
            }
        }
    }//GEN-LAST:event_brnSalvarActionPerformed

